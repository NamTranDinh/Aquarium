            <p>AquariumNexus was created to help beginners. My know-how will help you find the right fish species for your aquarium and the best products in each category</p>
            <p>I’m not a veterinarian, but I’ve learned lots of things about different fish species over the last 20 years. I’ve been keeping fish since I was 12.</p>
            <img style="padding-top: 5px;padding-bottom:30px" src="/Aquarium/public/templates/upload/home/bg_header.jpg" alt="">
            <p>Most of the things I know, I’ve learned from books and through trial and errors. Because back in the days there was so little resource on the internet about the aquarium hobby, there was no other option.</p>
            <p>Today, there are still so many topics about the aquarium hobby which are not covered. So, I’ve decided to create this website to share my experience about keeping aquarium fish.</p>
            <p>My goal with AquariumNexus is to create an educational website, which covers a wide range of topics about different fish species. My intention is to help beginners get started in the aquarium hobby and motivate those who want to quit because of failed aquarium projects.</p>
            <p>I invite you to browse through this website and gather all the information you need. If you still have questions, don’t hesitate to contact me.</p>
            <p>Wish you all the best, and happy fish keeping!</p>
            <p>Son Cong Nguyen</p>
