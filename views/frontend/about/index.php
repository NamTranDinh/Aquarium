            <h2 style="width: 70%;font-weight: 900;">Inspiring awareness & preservation of ocean and aquatic animals worldwide</h2>
            <div class="down_info">
                <div class="left_info">
                    <p>Georgia Aquarium, located in Atlanta, Georgia, is a 501(<span style="color:rgba(107, 245, 135, 0.816)">c</span>)(<span style="color:rgba(107, 245, 135, 0.816)">3</span>) non-profit organization that contains more than 11 million gallons of water. Georgia Aquarium is a scientific institution that entertains and educates, features exhibits and programs of the highest standards, and offers engaging and exciting guest experiences that promote the conservation of aquatic biodiversity throughout the world. As a leader in aquatic research and exceptional animal care, we are dedicated to fostering a deeper appreciation for our ocean and the animals that call it home.</p>
                </div>
                <div class="right_info">
                    <p>Alongside other accredited facilities, our team <span style="color:#f8c51c">conducts crucial</span> research by working with animals both in human care and in their natural habitats to improve husbandry methods, develop innovative and exciting new exhibits, contribute to the understanding of the underwater world and apply new discoveries to the conservation of aquatic life. Every day, researchers in the Aquarium’s exhibits and labs are learning more about marine life in order to develop new methods of animal care and veterinary medicine. By combining field research with the study of on-site animals in a controlled environment, the Aquarium is contributing to the advancement of human knowledge in the area of animal science.</p>
